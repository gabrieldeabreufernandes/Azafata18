public class AppMenuBar {
    String bgColor;
    String btnDisabled;
    String btnNormal;
    String btnPressed;
    String btnSelected;

    public String getBgColor() {
        return bgColor;
    }

    public void setBgColor(String bgColor) {
        this.bgColor = bgColor;
    }

    public String getBtnDisabled() {
        return btnDisabled;
    }

    public void setBtnDisabled(String btnDisabled) {
        this.btnDisabled = btnDisabled;
    }

    public String getBtnNormal() {
        return btnNormal;
    }

    public void setBtnNormal(String btnNormal) {
        this.btnNormal = btnNormal;
    }

    public String getBtnPressed() {
        return btnPressed;
    }

    public void setBtnPressed(String btnPressed) {
        this.btnPressed = btnPressed;
    }

    public String getBtnSelected() {
        return btnSelected;
    }

    public void setBtnSelected(String btnSelected) {
        this.btnSelected = btnSelected;
    }
}
